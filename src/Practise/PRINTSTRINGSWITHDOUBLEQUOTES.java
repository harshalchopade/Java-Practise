package Practise;

public class PRINTSTRINGSWITHDOUBLEQUOTES {
	
	//to escape character use '\' before and after the word
	
	public static void printWithinDoubleQuotes() {
		
		System.out.println("/\"Harshal\"/");
		System.out.println("'Harshal'");
		System.out.println("I love \"java\" and programming  and movies.");

	}

	public static void main(String[] args) {
		printWithinDoubleQuotes();
	}

}
