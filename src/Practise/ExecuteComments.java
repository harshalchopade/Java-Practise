package Practise;

public class ExecuteComments {

	/* '\u000d' represent Unicode carriage returns i.e generate new line */

	public static void main(String[] args) {

		// String str = "Hello dude \u000d System.out.println("hiiiiiii");
		// this is my code \u000d System.out.println("oooooooooooooooo");
		System.out.println("Hello");
		int value = 10;
		// \u000d value = 20;
		System.out.println(value);
	}

}
